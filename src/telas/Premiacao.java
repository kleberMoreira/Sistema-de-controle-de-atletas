    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        String titulo = tfTitulo.getText();
        String ano = tfAno.getText();
        
        int Ano;
        
        try {
            Ano = Integer.parseInt(ano);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Digite o ano corretamente");
            return;
        }
        
        premiacaoClass premio = new premiacaoClass(titulo, ano);
        
        cadastroController cadastro = new cadastroController();
        
        cadastro.cadastrarPremio(premio);
        
        if(cadastro.cadastrarPremio(premio))
        {
            JOptionPane.showMessageDialog(this, "Dados cadastrados com sucesso");
            this.dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "Erro!! Titulo n√£o cadastrado corretamente");
    }//GEN-LAST:event_btSalvarActionPerformed

